// Централизованные “календарные” константы, чтобы провайдеры/синк/уведомления работали согласованно.

/** На сколько дней вперёд мы получаем/парсим события из источников (CalDAV/ICS). */
export const CALENDAR_EVENTS_HORIZON_DAYS = 60;

/** На сколько дней вперёд мы создаём/синхронизируем заметки встреч (защита от “бесконечных” лент). */
export const NOTES_SYNC_HORIZON_DAYS = 60;

/** На сколько часов вперёд мы планируем таймеры уведомлений (защита от слишком большого числа таймеров). */
export const NOTIFICATIONS_HORIZON_HOURS = 48;

/** Миллисекунд в минуте. */
export const MS_PER_MINUTE = 60_000;
/** Миллисекунд в часе. */
export const MS_PER_HOUR = 60 * MS_PER_MINUTE;
/** Миллисекунд в дне. */
export const MS_PER_DAY = 24 * MS_PER_HOUR;
